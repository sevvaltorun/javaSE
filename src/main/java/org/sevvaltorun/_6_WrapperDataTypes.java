package org.sevvaltorun;

public class _6_WrapperDataTypes  {
    public static void main(String[] args) {
        /*
        Byte: byte türünü sarmalar
        Short: short türünü sarmalar
        Integer: int türünü sarmalar
        Long: long türünü sarmalar

        Float: float türünü sarmalar
        Double: double türünü sarmalar

        Boolean: boolean türünü sarmalar
        Character: char türünü sarmalar
         */

        Integer myInt = new Integer(10);
        Double myDouble= new Double(3.14);
        Boolean myBoolean = new Boolean(true);
        Character myChar= new Character('B');

        System.out.println(myInt);
        System.out.println(myDouble);
        System.out.println(myBoolean);
        System.out.println(myChar);


    }
}
